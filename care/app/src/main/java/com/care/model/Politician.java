package com.care.model;

public class Politician {
    public String name;
    public String type;
    public String state;
    public String party;
    public String twitter;

    public Politician(String name, String type, String state, String party, String twitter) {
        this.name = name;
        this.type = type;
        this.state = state;
        this.party = party;
        this.twitter = twitter;
    }
}
